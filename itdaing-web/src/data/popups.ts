// 주소/장소명에서 구 추정
function deriveDistrict(address: string, location: string): string | undefined {
  const src = `${address} ${location}`;
  if (/동구/.test(src)) return "동구";
  if (/서구/.test(src)) return "서구";
  if (/남구/.test(src)) return "남구";
  if (/북구/.test(src)) return "북구";
  if (/광산구/.test(src)) return "광산구";
  return undefined;
}

// 로드 시 district 채우기 (불변성 유지 위해 map)
for (let i = 0; i < popups.length; i++) {
  if (!popups[i].district) {
    popups[i].district = deriveDistrict(popups[i].address, popups[i].location);
  }
}

// 헬퍼 함수들
export function getPopupById(id: number): PopupData | undefined {
  return popups.find(popup => popup.id === id);
}

export function getActivePopups(): PopupData[] {
  return popups.filter(p => p.status === "ongoing" || p.status === "upcoming");
}

export function getPopupsByDistrict(district: string): PopupData[] {
  return popups.filter(p => (p.district ?? deriveDistrict(p.address, p.location)) === district);
}

export function getPopupsBySellerId(sellerId: number): PopupData[] {
  return popups.filter(popup => popup.sellerId === sellerId);
}